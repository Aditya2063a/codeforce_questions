#include <bits/stdc++.h>
using namespace std;

#define f first
#define s second
#define pb push_back

typedef long long int ll;
typedef unsigned long long int ull;
typedef pair<int,int> pii;
typedef pair<ll,ll> pll;

#define mod 1000000007
#define INF 1000000000

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);
    cout.tie(0);

    int tt;
    cin >> tt;
    while (tt--) {
        int n; cin>>n;
        vector<int> a(n);
        for(int i=0; i<n; i++) cin>>a[i];

        long long ans;

        if (a[0] != -1 && a[n-1] != -1) {
            ans = llabs(a[n-1] - a[0]);
        } else {
            ans = 0;
        }

        if (a[0] == -1 && a[n-1] == -1) {
            a[0] = 0;
            a[n-1] = 0;
        } 
        else if (a[0] == -1) {
            a[0] = a[n-1];
        }
        else if (a[n-1] == -1) {
            a[n-1] = a[0];
        }

        for (int i = 1; i < n-1; i++) {
            if (a[i] == -1) a[i] = 0;
        }

        cout << ans << "\n";
        for (int i = 0; i < n; i++) {
            cout << a[i] << (i + 1 < n ? ' ' : '\n');
        }
    }
}